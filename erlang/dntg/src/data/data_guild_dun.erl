		dun1_loop  -> 10*60;				  %% 关卡1持续时间
		dun1_scene -> 701;				  %% 关卡1场景
		dun1_born -> [56,34];			  %% 关卡1出生点	
		dun2_scene -> 702;                %% 关卡2场景
		dun2_born -> [50,25];			  %% 关卡2出生点
		dun2_loop  -> 2*60;				  %% 关卡2持续时间
		dun3_scene -> 703;				  %% 关卡3场景
		dun3_born -> [20,32];			  %% 关卡3出生点
        dun3_answer_xy -> [15,26];        %% 关卡3答题坐标
