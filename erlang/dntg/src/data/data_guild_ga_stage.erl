	case SkillType < 0 orelse SkillType > 10 orelse SKillLevel < 0 orelse SKillLevel > 150 of
		true ->
			{{0, 0}, 0, {0, 0}, {0,1}};
		false ->
			lists:keyfind({SkillType, SKillLevel}, 1, TupleList)
	end.

get_skill_cost(SKillLevel) ->
	TupleList =  skill_cost_base(),
	lists:keyfind(SKillLevel, 1, TupleList).

