%% 升级VIP的价格
get_up_cost(NowType, Type) ->
    case NowType of
        %% 原价购买
        0 ->
            case Type of
                1 -> 98;
                2 -> 388;
                _ -> 1988
            end;
        _ ->
            case NowType =:= Type of
                %% 续费原价购买
                true ->
                    case Type of
                        1 -> 98;
                        2 -> 388;
                        _ -> 1988
                    end;
                %% 升级9折购买
                false ->
                    case Type of
                        1 -> 98;
                        2 -> 349;
                        _ -> 1789
                    end
            end
    end.

