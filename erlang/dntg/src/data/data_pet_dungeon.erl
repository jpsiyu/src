%% 得到BOSS的ID.
get_boss_id(PlayerLevel) ->
	if 
	  PlayerLevel >= 30 andalso PlayerLevel =< 42 -> 23323; %镇压天兵.
	  PlayerLevel >= 43 andalso PlayerLevel =< 49 -> 23324; %镇压天兵.
	  PlayerLevel >= 50 andalso PlayerLevel =< 59 -> 23325; %镇压天兵.
	  PlayerLevel >= 60 andalso PlayerLevel =< 100 -> 23326; %镇压天兵.
	  true -> 23323 %镇压天兵.
	end.

